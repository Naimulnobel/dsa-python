#list concatenation
a=[1,2,3]
b=[4,5,6]
c=a+b
print(c)

#list repetition
d=a*3
print(f'repeated list is {d} and length of list is {len(d)}')
#length of list
print(f'length of list is {len(d)}')

#max of list
print(f'max of list is {max(d)}')
#min of list
print(f'min of list is {min(d)}')
#sum of list
print(f'sum of list is {sum(d)}')

#average of list
print(f'average of list is {sum(d)/len(d)}')
